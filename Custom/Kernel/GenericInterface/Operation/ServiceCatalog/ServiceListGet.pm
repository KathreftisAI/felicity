sub new {
    my ( $Type, %Param ) = @_;

    my $Self = {};
    bless( $Self, $Type );

    # check needed objects
    for my $Needed (qw(DebuggerObject WebserviceID)) {
        if ( !$Param{$Needed} ) {
            return {
                Success      => 0,
                ErrorMessage => "Got no $Needed!",
            };
        }

        $Self->{$Needed} = $Param{$Needed};
    }

    return $Self;
}



sub Run {
    my ( $Self, %Param ) = @_;

    my $Result = $Self->Init(
        WebserviceID => $Self->{WebserviceID},
    );

    if ( !$Result->{Success} ) {
        return $Self->ReturnError(
            ErrorCode    => 'Webservice.InvalidConfiguration',
            ErrorMessage => $Result->{ErrorMessage},
        );
    }

    my ( $UserID, $UserType ) = $Self->Auth(
        %Param,
    );

    return $Self->ReturnError(
        ErrorCode    => 'ServiceListGet.AuthFail',
        ErrorMessage => "ServiceListGet: Authorization failing!",
    ) if !$UserID;

    my $ErrorMessage = '';

    # all needed variables
   
    my $ReturnData    = {
        Success => 1,
    };
    my @Item;

    @Item = (123);
    
    
    # set ticket data into return structure
    $ReturnData->{Data}->{ServiceList} = \@Item;

    # return result
    return $ReturnData;
}

1;

